// タグをURLパスのセグメントに変換するためのヘルパー。
// Astro は日本語などのマルチバイト文字を自動でエンコードするが、
// 生の `%` はパーセントエスケープの開始とみなされ静的パスのマッチングが壊れる。
// （例: タグ「4%ルール」→ /tags/4%ルール/ がビルドエラーになる）
// そこで `%` だけを安全なトークンに置換し、ルート生成側とリンク側で同じ関数を使って整合させる。
// `%` を含まない既存タグには影響しない（＝既存URLは変わらない）。
const PERCENT_TOKEN = '-pct-';

export function tagSlug(tag: string): string {
	return tag.replace(/%/g, PERCENT_TOKEN);
}
